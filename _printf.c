#include "main.h"
#include <limits.h>
#include <stdio.h>

/**
 * _printf - Custom printf function with format specifier support.
 *
 * This function processes a format string and variable arguments to produce
 * formatted output. It recognizes and handles various format specifiers.
 *
 * @format: The format string.
 * @...: Variable number of arguments.
 *
 * Return: The number of characters printed on success, -1 on failure.
 */

int _printf(const char *format, ...)
{
	int (*pfunc)(va_list, flags_t *);
	const char *p;
	va_list arguments;
	flags_t flags = {0, 0, 0};

	register int count = 0;

	va_start(arguments, format);
	if (!format || (format[0] == '%' && !format[1]))
		return (-1);
	if (format[0] == '%' && format[1] == ' ' && !format[2])
		return (-1);
	for (p = format; *p; p++)
	{
		if (*p == '%')
		{
			p++;
			if (*p == '%')
			{
				count += _putchar('%');
				continue;
			}
			while (get_flag(*p, &flags))
				p++;
			pfunc = get_print(*p);
			count += (pfunc)
				? pfunc(arguments, &flags)
				: _printf("%%%c", *p);
		} else
			count += _putchar(*p);
	}
	_putchar(-1);
	va_end(arguments);
	return (count);

}

